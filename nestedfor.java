public class nestedfor {
	
	public static void main(String[] args)
	{
		
		int i ;
		int j; 
	for(i = 1; i <= 5; i++)
		{		System.out.println("here"); 
		    for(j = 1; j <= i; j++)  
		    {   
		    	
		        System.out.println(i); 
		         
		    }      
		    System.out.print("\n");        
		}
	}
}
			
				
				
				
